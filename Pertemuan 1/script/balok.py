print("Aplikasi menghitung luas permukaan dan volume balok")

# Atur nilai variabel
panjang = 20
lebar = 4
tinggi = 6

# Rumus
luas_permukaan = 2 * (panjang * lebar + panjang * tinggi + lebar * tinggi)
volume = panjang * lebar * tinggi

# Output
print("Panjang =", panjang)
print("Lebar =", lebar)
print("Tinggi =", tinggi)
print("Luas Permukaan =", luas_permukaan)
print("Volume =", volume)

import math

print("Aplikasi menghitung luas permukaan dan volume bola")

# Atur nilai variabel
jari_jari = 3.0

# Rumus
luas_permukaan = 4 * math.pi * jari_jari**2
volume = (4/3) * math.pi * jari_jari**3

# Output
print("Jari-Jari Bola =", jari_jari)
print("Luas Permukaan =", luas_permukaan)
print("Volume =", volume)
